#include <stdio.h>

int main() {
 const int number = 30;
 const float re = 6.42;
 const char sym = '@';
 printf("%d\n",number);
 printf("%f\n",re);
 printf("%d\n",sym);
 return 0;
}
/**constants are fixed  values ,there are mainly 3types
1.integer {...,-3,-2,-1,0,1,2,3,..} we use to print{%d}
2.Real{....,-3.0,-2.0,-1.0,0,1.0,2.0,...} we use to print{%f}
3.Character{'#','&','a','A',}{%c}*/